const { Schema, model } = require('mongoose')

const AccountSchema = new Schema({
    firstName: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    }
})


const Account = model('account', AccountSchema)

module.exports = Account